let current = null;

function read() {
    try {
        const v = localStorage.getItem(STORAGE_KEY);
        return AVAILABLE.indexOf(v) !== -1 ? v : DEFAULT_LANG;
    } catch (e) {
        return DEFAULT_LANG;
    }
}

function get() {
    if (current == null) current = read();
    return current;
}

function set(lang) {
    const next = AVAILABLE.indexOf(lang) !== -1 ? lang : DEFAULT_LANG;
    current = next;
    try { localStorage.setItem(STORAGE_KEY, next); } catch (e) { }
    if (typeof document !== "undefined") document.documentElement.lang = next;
    Bus.emit("lang", next);
}

function t(key, params) {
    const lang = get();
    const dict = DICT[lang] || DICT[DEFAULT_LANG];
    let s = dict[key] != null ? dict[key] : (DICT.en[key] != null ? DICT.en[key] : key);
    if (params) {
        Object.keys(params).forEach((k) => {
            s = s.replace(new RegExp("{" + k + "}", "g"), () => String(params[k]));
        });
    }
    return s;
}

function cat(name) {
    const map = CATEGORIES[get()] || CATEGORIES[DEFAULT_LANG];
    return (map && map[name]) || name;
}

function exists(key) {
    const dict = DICT[get()] || DICT[DEFAULT_LANG];
    return dict[key] != null || DICT.en[key] != null;
}

export const I18n = {
    available: AVAILABLE,
    get,
    set,
    t,
    cat,
    exists,
};